import java.util.*

fun main(args: Array<String>) {
    var scanner = Scanner(System.`in`)

    var line1 = scanner.nextLine()
    var line2 = scanner.nextLine()
    var nextInt1 = scanner.nextInt()
    var nextInt2 = scanner.nextInt()

    println(line1)
    println(line2)
    println(nextInt1)
    println(nextInt2)
}
